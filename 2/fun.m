function [F, J] = fun(X)
 % Определяем размерность переменных F и J
 n=size(X,1);
 F=zeros(n,1);
 J=zeros(n,n);
 
 % Вектор-функция F = [F1;F2] левых частей уравнений системы
 F(1)=-((X(1)+1).^3) + 2 - X(2);
 F(2)=(1/7)*X(1).^3 + 4 - X(2);
 
 % Матрица первых производных (Якобиан)
 J(1,1)=-3*X(1).^2-6*X(1)-3; % dF1/dX1
 J(1,2)=-1;                  % dF1/dX2
 J(2,1)=(3/7)*X(1).^2;       % dF2/dX1
 J(2,2)=-1;                  % dF2/dX2
 
end
