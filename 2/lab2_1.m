x1=-5:0.1:5;
f1=-((x1+1).^3) + 2;
f2=(1/7)*x1.^3 + 4;

% графики сечений поверхностей z=F1(x1,x2) и z=F2(x1,x2) плоскостью z=0
figure(1); 
plot(x1,f1,'b',x1,f2,'r');
axis([-5 5 -10 10]);
xlabel('x1');
ylabel('x2');
grid on;
legend('x2=f1(x1)','x2=f2(x1)');
title ('Графики зависимостей x2=f(x1)');