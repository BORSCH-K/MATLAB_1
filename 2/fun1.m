function [F J] = fun1(X)
 % Определяем размерность переменных F и J
 n=size(X,1);
 F=zeros(n,1);
 J=zeros(n,n);
 
 % Вектор-функция F = [F1;F2] левых частей уравнений системы
 F(1)=exp(-X(1))+cos(X(1))-X(2)-3;
 F(2)=2*(X(1)+2).^2-X(2)-8;
 
 % Матрица первых производных (Якобиан)
 J(1,1)=-exp(-X(1))-sin(X(1)); % dF1/dX1
 J(1,2)=-1;                    % dF1/dX2
 J(2,1)=4*(X(1)+2);            % dF2/dX1
 J(2,2)=-1;                    % dF2/dX2
 
end

